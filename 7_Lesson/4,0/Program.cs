int[,] FillArray(int rows, int columns)
{
    int[,] arr = new int[rows, columns];

    for (int i = 0; i < rows; i++)
    {
        for (int j = 0; j < columns; j++)
            arr[i, j] = i+j;
    }
    return arr;
}

